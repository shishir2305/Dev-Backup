// array methods are a collection of functions that can be used on arrays.
//
// // arr.push() is a method that adds an element to the end of an array.
//
// // arr.pop() is a method that removes the last element from an array.
//
// // arr.shift() is a method that removes the first element from an array.
//
// // arr.unshift() is a method that adds an element to the beginning of an array.
//
// // arr.splice() is a method that removes elements from an array.
//
// // arr.slice() is a method that returns a copy of a portion of an array.
//
// // arr.indexOf() is a method that returns the index of the first occurrence of an element in an array.
//
// // arr.lastIndexOf() is a method that returns the index of the last occurrence of an element in an array.
//
// // arr.map() is a method that creates a new array with the results of calling a function for every array element.
//
// // arr.filter() is a method that creates a new array with all array elements that pass a test.
//
// // arr.reduce() is a method that applies a function against an accumulator and each element in the array (from left to right) to reduce it to a single value.
//
// // arr.forEach() is a method that calls a function for each array element.
//
// // arr.every() is a method that tests whether all array elements pass a test.
//
// // arr.some() is a method that tests whether some array elements pass a test.
