// classlist methods

// classList.add() - adds a class to the element if it doesn't already exist and returns true if the class was added, false if it was already present in the element's class list

// classList.remove() - removes a class from the element if it exists and returns true if the class was removed, false if it was not present in the element's class list

// classList.toggle() - adds a class to the element if it doesn't already exist and removes it if it does exist and returns true if the class was added, false if it was already present in the element's class list

// classList.contains() - returns true if the element has the specified class, false if it doesn't
